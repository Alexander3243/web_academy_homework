# –¢–∞–∫ –∫–∞–∫ –º—ã –Ω–µ –∑–Ω–∞–µ–º –∫–∞–∫–æ–π —Ç–∏–ø —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—ã –±—É–¥–µ—Ç –≤–≤–æ–¥–∏—Ç—Å—è, —Ç–æ –Ω–∞ –≤—Ö–æ–¥ –ø—Ä–∏–Ω–∏–º–∞–µ–º —Å—Ç—Ä–æ–∫—É —Å –±—É–∫–≤–æ–π –≤ –∫–æ–Ω—Ü–µ.
# –° - —Ü–µ–ª—å—Å–∏–π, F - —Ñ–∞—Ä–µ–Ω–≥–µ–π—Ç

t = input()

if t[-1] == 'F':
    t = int(t[0:-1])
    print(round(5 / 9 * (t - 32), 2))
elif t[-1] == 'C':
    t = int(t[0:-1])
    print(round(9 / 5 * t + 32, 2))

"""
4. –ó—ñ —Å–ø–∏—Å–∫—É –≤–∏–ø–∞–¥–∫–æ–≤–∏—Ö —á–∏—Å–µ–ª,–≤–∏–∑–Ω–∞—á–∏—Ç–∏ —Ç–∞ –≤–∏–≤–µ—Å—Ç–∏ –Ω–∞ –µ–∫—Ä–∞–Ω –Ω–µ–ø–∞—Ä–Ω—ñ —á–∏—Å–ª–∞.
"""

# c–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª –ø—Ä–∏–Ω–∏–º–∞–µ—Ç—Å—è –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏ –Ω–∞–ø—Ä–∏–º–µ—Ä: 2019834579083574
# –Ω–µ–ø–∞—Ä–Ω—ã–º–∏ —á–∏—Å–ª–∞–º–∏ —Å—á–∏—Ç–∞–µ–º —Ç–µ —á–∏—Å–ª–∞, –∫–æ—Ç–æ—Ä—ã—Ö –≤ —Å–ø–∏—Å–∫–µ –±—É–¥–µ—Ç –º–µ–Ω—å—à–µ 2-—Ö

nums = [int(i) for i in input()]

for i in nums:
    if nums.count(i) < 2:
        print(i)

"""
5. –í–≤–æ–¥–∏—Ç—å—Å—è —Ü—ñ–ª–µ —á–∏—Å–ª–æ, —â–æ –æ–∑–Ω–∞—á–∞—î –∫–æ–¥ —Å–∏–º–≤–æ–ª—É –∑–∞ —Ç–∞–±–ª–∏—Ü–µ—é ASCII. 
–í–∏–∑–Ω–∞—á–∏—Ç–∏, —Ü–µ –∫–æ–¥ –∞–Ω–≥–ª—ñ–π—Å—å–∫–æ—ó –ª—ñ—Ç–µ—Ä–∏ –∞–±–æ –±—É–¥—å-—è–∫–∏–π —ñ–Ω—à–∏–π —Å–∏–º–≤–æ–ª.
"""
num = int(input())

if num in range(65, 91) or num in range(97, 123):
    print("–ë—É–∫–≤–∞:", chr(num))
else:
    print("–°–∏–º–≤–æ–ª:", chr(num))

"""
6. –í–≤–æ–¥—è—Ç—å—Å—è –¥–≤–∞ —Ü—ñ–ª–∏—Ö —á–∏—Å–ª–∞. –ü–µ—Ä–µ–≤—ñ—Ä–∏—Ç–∏ —á–∏ –¥—ñ–ª–∏—Ç—å—Å—è –ø–µ—Ä—à–µ –Ω–∞ –¥—Ä—É–≥–µ. 
–í–∏–≤–µ—Å—Ç–∏ –Ω–∞ –µ–∫—Ä–∞–Ω –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ —Ü–µ, –∞ —Ç–∞–∫–æ–∂ –∑–∞–ª–∏—à–æ–∫ (—è–∫—â–æ –≤—ñ–Ω —î) —Ç–∞ –ø—Ä–∏–≤–∞—Ç–Ω–µ (—É –±—É–¥—å-—è–∫–æ–º—É –≤–∏–ø–∞–¥–∫—É).
"""

# –Ω–µ–º–Ω–æ–≥–æ –Ω–µ–ø–æ–Ω—è—Ç–Ω–æ —É—Å–ª–æ–≤–∏–µ, –±—É–¥–µ–º —Å—á–∏—Ç–∞—Ç—å, —á—Ç–æ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ –¥–æ–ª–∂–Ω–æ –¥–µ–ª–∏—Ç—Å—è –Ω–∞ –≤—Ç–æ—Ä–æ–µ –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞
num1 = int(input())
num2 = int(input())

if num1 % num2 == 0:
    print("num1 –¥–µ–ª–∏—Ç—Å—è –Ω–∞ num2 –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞")
    print("—á–∞—Å—Ç–Ω–æ–µ =", num1 / num2)
elif num1 % num2 != 0:
    print("–æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è num1 –Ω–∞ num2 =", num1 % num2)
    print("—á–∞—Å—Ç–Ω–æ–µ =", num1 / num2)

"""
7. –ü–æ —á–µ—Ä–∑—ñ –≤–≤–æ–¥–∏—Ç—å—Å—è 5 —Ü–∏—Ñ—Ä, –≤–∏–≤–µ—Å—Ç–∏ —ó—Ö–Ω—é —Å—É–º—É (—Å–∫–æ—Ä–∏—Å—Ç–∞–≤—à–∏—Å—å for)
"""
result = int()

for i in range(5):
    result += int(input())

